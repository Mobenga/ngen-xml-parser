package com.mobenga.ngen.xml.parser.example.model;

/**
 * This is only a Test Outcome used to document XML Parser
 */
public class Outcome {
    private String id;
    private String name;
    private String odds;
    private String oddsDecimal;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOdds() {
        return odds;
    }

    public void setOdds(String odds) {
        this.odds = odds;
    }

    public String getOddsDecimal() {
        return oddsDecimal;
    }

    public void setOddsDecimal(String oddsDecimal) {
        this.oddsDecimal = oddsDecimal;
    }
}
